# 3. Support agent
# ---------------------------------------------------------------------------

def support_agent(message: str, context: dict[str, Any]) -> dict[str, Any]:
    """
    Handles product support issues.
    RAG always runs here — support needs the playbook to give accurate steps.
    """
    docs = retrieve(query=message, kb_name="support", top_k=1)

    playbook_text = ""
    best_doc = None
    if docs and docs[0].get("_score", 0) > 0.3:
        best_doc = docs[0]
        playbook = best_doc.get("playbook", {})
        steps = playbook.get("steps", [])
        follow_up = playbook.get("follow_up_question", "")
        summary = playbook.get("summary", "")
        playbook_text = f"Summary: {summary}\nSteps:\n" + "\n".join(f"- {s}" for s in steps)
        if follow_up:
            playbook_text += f"\nFollow-up question to ask if details are missing: {follow_up}"

    prompt = SUPPORT_PROMPT.format(
        message=message,
        playbook=playbook_text or "No specific playbook found. Ask a clarifying question.",
    )
    response = _chat(NOVA_SYSTEM_PROMPT, prompt)

    workflow_card = None
    if best_doc:
        steps = best_doc.get("playbook", {}).get("steps", [])
        if steps:
            workflow_card = {
                "type": "support_steps",
                "title": best_doc.get("title", "Troubleshooting Steps"),
                "items": steps,
            }

    return {
        "response": response,
        "tools_used": [],
        "used_vector_search": True,
        "citations": [{"title": best_doc["title"], "source": "NexaFi Support KB"}] if best_doc else [],
        "state": None,
        "workflow_card": workflow_card,
    }


