# 4. Retention agent
# ---------------------------------------------------------------------------

def retention_agent(message: str, context: dict[str, Any]) -> dict[str, Any]:
    """
    Handles churn risk signals.
    Churn is only logged when a doc was actually matched (score > 0.2).
    """
    user_id = context.get("user_id", "unknown")

    docs = retrieve(query=message, kb_name="retention", top_k=1)

    guidance_text = ""
    best_doc = None
    churn_logged = False

    if docs and docs[0].get("_score", 0) > 0.2:
        best_doc = docs[0]
        guidance = best_doc.get("guidance", {})
        empathy = guidance.get("empathy_line", "")
        fixes = guidance.get("proposed_fixes", [])
        follow_up = guidance.get("follow_up_question", "")
        guidance_text = (
            f"Empathy line: {empathy}\n"
            f"Proposed fixes:\n" + "\n".join(f"- {f}" for f in fixes) +
            (f"\nFollow-up: {follow_up}" if follow_up else "")
        )
        # Only log churn when we have a confident match
        concern_type = best_doc.get("concern_type", "general")
        log_churn_reason(user_id, concern_type)
        churn_logged = True

    prompt = RETENTION_PROMPT.format(
        message=message,
        guidance=guidance_text or "Acknowledge frustration and ask what specifically isn't working.",
    )
    response = _chat(NOVA_SYSTEM_PROMPT, prompt, temperature=0.5)

    fixes = best_doc.get("guidance", {}).get("proposed_fixes", []) if best_doc else []
    workflow_card = {
        "type": "retention_offer",
        "title": "We want to make this right",
        "items": fixes,
    } if fixes else None

    return {
        "response": response,
        "tools_used": ["log_churn_reason"] if churn_logged else [],
        "used_vector_search": True,
        "citations": [],
        "state": None,
        "workflow_card": workflow_card,
    }


# ---------------------------------------------------------------------------
# 5. Profile agent
# ---------------------------------------------------------------------------

def profile_agent(message: str, context: dict[str, Any]) -> dict[str, Any]:
    """
    Handles profile transparency questions.
    No RAG needed — fetches user profile directly.
    """
    user_id = context.get("user_id", "unknown")
    profile = get_user_profile(user_id)

    if not profile:
        return {
            "response": (
                "I don't have a profile on file for your account yet. "
                "You can set up your investor profile in Settings > Profile."
            ),
            "tools_used": ["get_user_profile"],
            "used_vector_search": False,
            "citations": [],
            "state": None,
            "workflow_card": None,
        }

    profile_text = json.dumps(profile, indent=2)
    prompt = PROFILE_PROMPT.format(profile=profile_text)
    response = _chat(NOVA_SYSTEM_PROMPT, prompt)

    return {
        "response": response,
        "tools_used": ["get_user_profile"],
        "used_vector_search": False,
        "citations": [],
        "state": {
            "known": profile.get("known", {}),
            "inferred": profile.get("inferred", {}),
            "missing": profile.get("missing", []),
        },
        "workflow_card": None,
    }


# ---------------------------------------------------------------------------
# 6. Fallback agent
# ---------------------------------------------------------------------------

def fallback_agent(message: str, context: dict[str, Any]) -> dict[str, Any]:
    """Off-topic or unclassifiable — no tools, no RAG, no LLM."""
    return _empty_result(OFF_TOPIC_RESPONSE)
